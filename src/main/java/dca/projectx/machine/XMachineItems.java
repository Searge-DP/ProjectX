package dca.projectx.machine;

import cpw.mods.fml.common.registry.GameRegistry;
import dca.projectx.machine.item.XItemEnergyCore;
import net.minecraft.item.Item;

public class XMachineItems {
	
	public static Item energyCoreMK1;
	public static Item energyCoreMK2;
	public static Item energyCoreMK3;
	
	public static void preInit(){
		GameRegistry reg = null;
		reg.registerItem(energyCoreMK1 = (new XItemEnergyCore("energyCoreMK1")), "energyCoreMK1");
		reg.registerItem(energyCoreMK2 = (new XItemEnergyCore("energyCoreMK2")), "energyCoreMK2");
		reg.registerItem(energyCoreMK3 = (new XItemEnergyCore("energyCoreMK3")), "energyCoreMK3");
	}

}
